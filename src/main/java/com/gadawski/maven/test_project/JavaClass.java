package com.gadawski.maven.test_project;


/**
 * Simple java class for Clojure example.
 * 
 * @author Lukasz Gadawski
 *
 */
public class JavaClass {
    
    public static boolean checkParamAGreaterThanB(int a, int b) {
        return a > b;
    }

    public static boolean checkParamALessThanB(int a, int b) {
        return a < b;
    }

    public static boolean checkParamALessOrEqualThanB(int a, int b) {
        return a <= b;
    }
}
